<?php
class Participante{
    //Atts
    private $id; 
    private $nombre; 
    private $edad; 
    private $genero; 
    private $dni; 
    private $email; 
    private $zombie; 
//Constructor
public function __construct($id,$edad,$genero,$dni,$zombie){
    $this->nombre=$nombre;
    $this->edad=$edad;
    $this->genero=$genero; 
    $this->dni=$dni; 
    $this->email=$email; 
    $this->zombie=$zombie; 
}



    /**
     * Get the value of id
     */ 
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */ 
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of nombre
     */ 
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set the value of nombre
     *
     * @return  self
     */ 
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get the value of edad
     */ 
    public function getEdad()
    {
        return $this->edad;
    }

    /**
     * Set the value of edad
     *
     * @return  self
     */ 
    public function setEdad($edad)
    {
        $this->edad = $edad;

        return $this;
    }

    /**
     * Get the value of genero
     */ 
    public function getGenero()
    {
        return $this->genero;
    }

    /**
     * Set the value of genero
     *
     * @return  self
     */ 
    public function setGenero($genero)
    {
        $this->genero = $genero;

        return $this;
    }

    /**
     * Get the value of dni
     */ 
    public function getDni()
    {
        return $this->dni;
    }

    /**
     * Set the value of dni
     *
     * @return  self
     */ 
    public function setDni($dni)
    {
        $this->dni = $dni;

        return $this;
    }

    /**
     * Get the value of email
     */ 
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * Set the value of email
     *
     * @return  self
     */ 
    public function setEmail($email)
    {
        $this->email = $email;

        return $this;
    }

    /**
     * Get the value of zombie
     */ 
    public function getzombie()
    {
        return $this->zombie;
    }

    /**
     * Set the value of zombie
     *
     * @return  self
     */ 
    public function setzombie($zombie)
    {
        $this->zombie = $zombie;

        return $this;
    }
}


?>